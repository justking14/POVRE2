function getRandomColor() {
    var letters = '0123446789ABCDEF';
    var color = '#';
    for (var i = 0; i < 6; i++) {
        color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
}

// Collision boolean (useful for passive collision detection)
function collision(A, B) {
    var Atop = A.y;
    var Abottom = A.y + A.h;
    var Aleft = A.x;
    var Aright = A.x + A.w;

    var Btop = B.y;
    var Bbottom = B.y + B.h;
    var Bleft = B.x;
    var Bright = B.x + B.w;

    return !( (Abottom<Btop) || (Atop>Bbottom) || (Aright<Bleft) || (Aleft>Bright) );
}

function isEmpty(obj) {
    for(var prop in obj) {
        if(obj.hasOwnProperty(prop))
            return false;
    }

    return true;
}
